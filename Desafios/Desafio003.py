#Crie um programa que leia dois numeros e mostre a soma entre eles.
n1 = float(input("Informe um numero: "))
n2 =  float(input('Informe outro numero: '))

soma: float = n1 + n2
print(f'A soma entre {n1} e {n2} vale {soma}')
