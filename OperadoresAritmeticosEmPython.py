###     Operadores Aritmeticos
#       + --> Adição
#       - --> Subtração
#       * --> Multiplicação
#       / --> Divisão
#       ** --> Potência
#       // --> Divisão Inteira
#       %  --> Resto da Divisão (MOD)
#       Todos os Operadores precisam de dois operadores (Operadores Binários)
#       
#       Ordem de Precedência
#   1-) ()
#   2-) **
#   3-) *   /    //  %
#   4-) +   -
